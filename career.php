// JavaScript for handling dynamic skills
document.addEventListener('DOMContentLoaded', function() {
    const skillsContainer = document.getElementById('skills-container');
    const addSkillBtn = document.getElementById('add-skill-btn');

    // Add new skill row
    addSkillBtn.addEventListener('click', function() {
        const skillRow = document.createElement('div');
        skillRow.className = 'skill-row d-flex gap-2 mb-2';
        skillRow.innerHTML = `
                <input type="text" class="form-control ca-two-border" name="skill_name[]" placeholder="Skill Name (e.g., HTML, CSS, JavaScript, Photoshop)" required style="flex: 2;">
                <input type="number" class="form-control ca-two-border" name="skill_percentage[]" placeholder="% (e.g., 90)" min="0" max="100" required style="flex: 1;">
                <button type="button" class="btn btn-outline-danger remove-skill-btn" style="width: 40px;" title="Remove">×</button>
              `;
        skillsContainer.appendChild(skillRow);

        // Add remove event to new button
        skillRow.querySelector('.remove-skill-btn').addEventListener('click',
            function() {
                skillRow.remove();
            });
    });

    // Remove skill row (for first row - just clear values)
    skillsContainer.addEventListener('click', function(e) {
        if (e.target.classList.contains('remove-skill-btn')) {
            const rows = skillsContainer.querySelectorAll('.skill-row');
            if (rows.length > 1) {
                e.target.closest('.skill-row').remove();
            } else {
                // For the first row, just clear the values
                const firstRow = rows[0];
                firstRow.querySelectorAll('input').forEach(input => input.value = '');
            }
        }
    });

    // Form submission handler to combine skills
    const form = document.querySelector('form[action="send4.php"]');
    form.addEventListener('submit', function(e) {
        const skillNames = document.getElementsByName('skill_name[]');
        const skillPercentages = document.getElementsByName('skill_percentage[]');
        let skillsArray = [];

        for (let i = 0; i < skillNames.length; i++) {
            if (skillNames[i].value && skillPercentages[i].value) {
                skillsArray.push(skillNames[i].value + ' (' + skillPercentages[i]
                    .value + '%)');
            }
        }

        document.getElementById('skills-combined').value = skillsArray.join(', ');
    });
});
</script>

