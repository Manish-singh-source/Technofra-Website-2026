(function() {
    const toggleBtn = document.getElementById("tfChatbotToggle");
    const closeBtn = document.getElementById("tfChatbotClose");
    const chatBox = document.getElementById("tfChatbotBox");
    const sendBtn = document.getElementById("tfChatbotSend");
    const input = document.getElementById("tfChatbotInput");
    const body = document.getElementById("tfChatbotBody");

    function toggleChat() {
        chatBox.classList.toggle("active");
        if (chatBox.classList.contains("active")) {
            setTimeout(() => input.focus(), 150);
        }
    }

    function appendMessage(text, type) {
        const msg = document.createElement("div");
        msg.className = "tf-msg " + (type === "user" ? "tf-msg-user" : "tf-msg-bot");
        msg.innerHTML = text;
        body.appendChild(msg);
        body.scrollTop = body.scrollHeight;
    }

    function botReply(userText) {
        let reply = "Thanks for your message. Our team will get back to you shortly.";

        const text = userText.toLowerCase();

        if (text.includes("website")) {
            reply = "We can help with Website Development, WordPress, Shopify, and custom business websites.";
        } else if (text.includes("app")) {
            reply = "We also build Android and iOS applications tailored to business needs.";
        } else if (text.includes("branding") || text.includes("ui") || text.includes("ux")) {
            reply = "Sure — we can help with Branding, UI/UX, and digital experience design.";
        } else if (text.includes("payment")) {
            reply = "We provide secure payment gateway integration for websites and apps.";
        } else if (text.includes("chatbot") || text.includes("bot")) {
            reply = "Yes, Technofra-style chatbot integration is available for your site.";
        }

        setTimeout(() => appendMessage(reply, "bot"), 500);
    }

    function sendMessage() {
        const text = input.value.trim();
        if (!text) return;
        appendMessage(text, "user");
        input.value = "";
        botReply(text);
