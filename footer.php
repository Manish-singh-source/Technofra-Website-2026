 <script>
(function() {
    const track = document.getElementById('tfxIndusTrack');
    const slides = document.querySelectorAll('.tfxIndusSlide');
    const prevBtn = document.querySelector('.tfxIndusPrev');
    const nextBtn = document.querySelector('.tfxIndusNext');
    const dots = document.querySelectorAll('.tfxIndusDot');

    let currentIndex = 1;
    let isAnimating = false;
    let autoSlide;

    const totalSlides = slides.length;

    function updateDots() {
        dots.forEach(dot => dot.classList.remove('active'));
        if (currentIndex === 1) dots[0].classList.add('active');
        else if (currentIndex === 2) dots[1].classList.add('active');
    }

    function moveToSlide() {
        track.style.transition = 'transform 0.75s ease-in-out';
        track.style.transform = `translateX(-${currentIndex * 100}%)`;
        updateDots();
    }

    function startAutoSlide() {
        autoSlide = setInterval(() => {
            goNext();
        }, 4000);
    }

    function resetAutoSlide() {
        clearInterval(autoSlide);
        startAutoSlide();
    }

    function goNext() {
        if (isAnimating) return;
        isAnimating = true;
        currentIndex++;
        moveToSlide();
    }

    function goPrev() {
        if (isAnimating) return;
        isAnimating = true;
        currentIndex--;
        moveToSlide();
    }

    track.addEventListener('transitionend', function() {
        if (currentIndex === totalSlides - 1) {
            track.style.transition = 'none';
            currentIndex = 1;
            track.style.transform = `translateX(-${currentIndex * 100}%)`;
        }

        if (currentIndex === 0) {
            track.style.transition = 'none';
            currentIndex = totalSlides - 2;
            track.style.transform = `translateX(-${currentIndex * 100}%)`;
        }

        updateDots();
        isAnimating = false;
    });

    nextBtn.addEventListener('click', function() {
        goNext();
        resetAutoSlide();
    });

    prevBtn.addEventListener('click', function() {
        goPrev();
        resetAutoSlide();
    });

    dots.forEach(dot => {
        dot.addEventListener('click', function() {
            if (isAnimating) return;
            const targetIndex = parseInt(this.getAttribute('data-index'));
            currentIndex = targetIndex;
            moveToSlide();
            resetAutoSlide();
        });
    });

    track.style.transform = `translateX(-${currentIndex * 100}%)`;
    updateDots();
    startAutoSlide();
})();
 </script>

